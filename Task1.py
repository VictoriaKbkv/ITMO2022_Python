V_sand = float(input("–°–∫–æ—Ä–æ—Å—Ç—å –¥–≤–∏–∂–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø–æ –ø–µ—Å–∫—É, Vùë†ùëéùëõùëë (–≤ –º–∏–ª—è—Ö –≤ —á–∞—Å): ")) * FEET_PER_MILE / SEC_PER_HOUR
n = float(input("–ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∑–∞–º–µ–¥–ª–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø—Ä–∏ –¥–≤–∏–∂–µ–Ω–∏–∏ –≤ –≤–æ–¥–µ, n (–≤ –µ–¥.): "))
theta1 = float(input("–ù–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –¥–≤–∏–∂–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø–æ –ø–µ—Å–∫—É, ùúÉ1 (–≤ –≥—Ä–∞–¥—É—Å–∞—Ö): ")) / DEGREES_PER_RADIAN

L1 = d1 / math.cos(theta1)
L2 = math.sqrt((h - L1 * math.sin(theta1)) ** 2 + d2 ** 2)
t = (L1 + n * L2) / V_sand

