import math

FEET_PER_YARD = 3
FEET_PER_MILE = 5280
DEGREES_PER_RADIAN = 57.2958
SEC_PER_HOUR = 3600

d1 = float(input("–ö—Ä–∞—Ç—á–∞–π—à–µ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –æ—Ç —Å–ø–∞—Å–∞—Ç–µ–ª—è –¥–æ –∫—Ä–æ–º–∫–∏ –≤–æ–¥—ã, ùëë1 (–≤ —è—Ä–¥–∞—Ö): ")) * FEET_PER_YARD
d2 = float(input("–ö—Ä–∞—Ç—á–∞–π—à–µ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –æ—Ç —É—Ç–æ–ø–∞—é—â–µ–≥–æ –¥–æ –±–µ—Ä–µ–≥–∞, ùëë2 (–≤ —Ñ—É—Ç–∞—Ö): "))
